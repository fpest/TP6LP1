    private void limpiarCampos() {
        DefaultTableModel model = (DefaultTableModel) jTblClienteTelefonoPorApellido.getModel();
        model.setRowCount(0);
    };
    
    private void llenarListaApellidos() {
        int largoMapa = MenuPrincipal.getDirectorio().getDirectorio().size();
        String[] apellidos = new String[largoMapa];
        int i = 0;

        Collection<String> setApellidos = new TreeSet();
        for (Cliente cliente : MenuPrincipal.getDirectorio().getDirectorio().values()) {
            //setApellidos.add(cliente.getApellido());
            
            if (setApellidos.add(cliente.getApellido())){
                if (cliente.getApellido().toUpperCase().contains(jTxtApellido.getText().toUpperCase())) {
                apellidos[i] = cliente.getApellido();
                i++;
                }
             };
            };

        jLstListaApellidos.removeAll();
        jLstListaApellidos.setModel(new javax.swing.AbstractListModel<String>() {
            public int getSize() {
                return apellidos.length;
        }
    
            public String getElementAt(int i) {
                return apellidos[i];
            }

        });
    }

    ;
    
      private void llenarTabla(Long telefono, Cliente cliente) {

        DefaultTableModel model = (DefaultTableModel) jTblClienteTelefonoPorApellido.getModel();
        model.addRow(new Object[]{cliente.getDni(), cliente.getApellido(), cliente.getNombre(), cliente.getDireccion(), cliente.getCiudad(), telefono});
    };
    
    private void jLstListaApellidosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jLstListaApellidosValueChanged
        limpiarCampos();
        if (!jLstListaApellidos.isSelectionEmpty()) {
            jTxtApellido.setText(jLstListaApellidos.getSelectedValue());
            for (Map.Entry<Long, Cliente> e : MenuPrincipal.getDirectorio().getDirectorio().entrySet()) {
                String apellido = e.getValue().getApellido();
                if (apellido.equals(jLstListaApellidos.getSelectedValue())) {
                    llenarTabla(e.getKey(), e.getValue());
                }
            }
        }
    }//GEN-LAST:event_jLstListaApellidosValueChanged

